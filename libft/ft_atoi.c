#include "libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			sign;
	size_t		result;

	sign = 1;
	i = 0;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - 48;
		if (result >= __LONG_MAX__ && sign == 1)
			return (-1);
		if (result > __LONG_MAX__ && sign == -1)
			return (0);
		i++;
	}
	return (result * sign);
}
